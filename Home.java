    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {                                          
        
   
        int t1 = Integer.parseInt(txtsearch.getText());     // get searh element from textfield
        Functions findingnode = searchTree.searchNode(t1);          // pass to Tree class
        
        if(findingnode != null){            
                       

            JOptionPane.showMessageDialog(null,"Book Details "+"\n"+"\n ISBN - "+findingnode.getId()+"\n First Name -"+findingnode.getFname()+"\n Surname - "+findingnode.getSname()+"\n Title - "+findingnode.getTitle()+"\n Book Name - "+findingnode.getBname());
                               }
        
        else
             //System.out.println("\n ID found not complete");
            JOptionPane.showMessageDialog(null,"\n ID is not in current Tree");
        
  
        
    }                                         

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {                                          

        int d1 = Integer.parseInt(txtsearch.getText());
        searchTree.delete(d1);
        
        sortData();  
        clear();
       
    
    }                                         

    public void sortData(){ 
        
        DTmodel = new DefaultTableModel(types,title2);
        tableBook.setModel(DTmodel);
        Functions root= searchTree.getRoot();                   //assign root node
        inOrder(root);                                 //pasising value to root node
        
    }
    
    public void inOrder(Functions r){
        
        if(r!=null){            
            inOrder(r.getLeft());                      
            DTmodel.addRow(new Object[]{r.id,r.fname,r.sname,r.title,r.bname});   //set data to table
            inOrder(r.getRight());           
            
        }
        
    }
    
    
