    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {                                          

        id    = Integer.parseInt(txtisbn.getText());
        fname = txtfname.getText();
        sname = txtsname.getText();
        title = cmbtitle.getSelectedItem().toString();
        bname = txtbookname.getText();
        
        
        
        Functions result = searchTree.searchNode(id);
        
        if(result != null){
            

        
        JOptionPane.showMessageDialog(null,+id+" "+"ISBN is already inserted");
            
                         }
        else{
        
        searchTree.Add(id,fname,sname,title,bname);

        sortData();
        
        }
        clear();

           
    }                                         

